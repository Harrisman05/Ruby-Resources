# Integers

# Integer converted to string

num = 65 

puts "Print my number " + num.to_s # to_s method

# Absolute value

num2 = -88

puts num.abs()

# Round value

num3 = 5.3
num4 = 66.90
num5 = 4.1

puts num3.round()  # rounds down
puts num4.round()  # rounds up
puts num5.ceil() # always rounds up
puts num5.floor() # always rounds down

# Using Math class

puts Math.sqrt(25)  

# Mathematics with integers/floats

puts 25/6 # this will round to the nearest whole number
puts 25/7
puts 25/6.1 # will provide the exact division value

